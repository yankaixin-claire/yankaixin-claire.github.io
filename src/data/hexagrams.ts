/** 八卦列表 — 用于查表 */
const TRIGRAMS: TrigramName[] = ["乾", "兑", "离", "震", "巽", "坎", "艮", "坤"];

/** 由上下卦 ID 查64卦序号（1-indexed） */
export function getHexagramIdByTrigrams(
  upper: TrigramName,
  lower: TrigramName
): number {
  const upperIdx = TRIGRAMS.indexOf(upper);
  const lowerIdx = TRIGRAMS.indexOf(lower);
  return upperIdx * 8 + lowerIdx + 1;
}

